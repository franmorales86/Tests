package com.reclabs.recomendar.core.exceptions.common;

import com.reclabs.recomendar.common.exceptions.generic.ErrorException;
import com.reclabs.recomendar.common.types.CodeErrorType;

/**
 * Indica que no se disponen de permisos para realizar una determinada acción.
 * @author raulanatol
 */
public class PermissionDeniedException extends ErrorException {
    private static final long serialVersionUID = 6011476479067577613L;

    /**
     * Constructor por defecto que introduce el código de permiso denegado.
     */
    public PermissionDeniedException() {
        super(CodeErrorType.PERMISSION_DENIED);
    }
}
